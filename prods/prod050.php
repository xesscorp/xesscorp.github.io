<h1>"I like the <a href="prod048.php">XuLA board</a>, but I've already got a lot of <abbr title="a registered trademark of Digilent Inc.">PMOD</abbr> boards..."</h1>
<p>Yeah, we've heard that <em>a lot.</em> So much that I began to feel like telling them to "stick it!" Then I started thinking: "Well, why <em>don't</em> they stick it!?" So we built a board that lets you connect your previously-purchased PMODs to a XuLA board. And, of course, we named it the "StickIt! board"</p>
<h1>"Umm, I also have a few Papilio Wing boards..."</h1>
<p>No problem! The StickIt! board has Wing sockets, too.</p>
<h1>"How do I use the StickIt! board ?"</h1>
<p>
Just connect your PMOD and Wing modules to the sockets along the edge of the StickIt! board, then insert a XuLA board in the center socket. Then load bitstreams from your PC through the USB port to program the FPGA and make it access the various StickIt! modules. All the features of the XuLA board are still available when you use the StickIt! board, so you can do things like use it in a stand-alone mode just by programming the Flash on the XuLA board and letting it configure the FPGA whenever the power comes on. It's really that simple.
</p>
<h1>"So I could use both PMOD and Wing boards?"</h1>
<p>Yes, you can connect up to six 4-bit PMODs, or three 8-bit PMODs, or three 8-bit Wing modules in any combination as long as all the modules require a total of 24 or fewer I/Os. The StickIt! board also lets you use an external power supply if your modules need more current than the XuLA board can provide.</p>
<h1>"Will XESS be building any modules for the StickIt! board ?"</h1>
<p>
Yes! You can see them listed below under "Related Products". You can insert our modules into either the PMOD or Wing sockets. They're also compatible with other systems that use PMOD and Wing modules.
</p>
<h1>"What's the future hold for the StickIt! board ?"</h1>
<p>We've learned we can't anticipate everything you want. And we don't have the resources to design and build every board variation you can think of. <em>So we gave up and made the entire StickIt! board design open-source.</em> <a href="https://github.com/xesscorp/StickIt">All of it: bitstreams, schematic, PCB layout, documentation &mdash; everything!</a> If you don't like what we built, you can re-design it and build it your way. And you can sell it to others free-and-clear of any royalties. (But you have to open-source your design just like we do.) And as others do this, you may find someone has built a version of the StickIt! board that <em>is</em> perfect for you.</p>
<h1>"Now what?"</h1>
<p>Now it's up to you. You can read the FAQ that follows to get more information, and peruse the <a href="../manuals/StickIt-manual-v3_0.pdf">StickIt! board Board manual</a> for all the details. <a href="mailto:info@xess.com?subject=StickIt! board Question">Send us an email</a> if you can't find an answer to your question. Then decide if you want to keep forcing your designs into an inflexible system, or if you'd rather be free to build a solution that fits.</p>
<p><em>It's really that simple.</em></p>
<h1 class="toggle_title">FAQ</h1>
<div class="toggle_body">
<h2 class="toggle_title">I ordered a StickIt! board and all I got was a board. No software? No manual? <em>No fancy box!?</em></h2>
<p class="toggle_body">When you order a StickIt! board, all you get is the board. That's it. The StickIt! board doesn't need any software itself. And the <a href="../manuals/StickIt-manual-v3_0.pdf">manual is online</a>. As for the fancy box, well, sorry about that. Think of it as "saving the environment".</p>
<h2 class="toggle_title">What kind of instructions are there for using the StickIt! board ?</h2>
<p class="toggle_body">The <a href="../manuals/StickIt-manual-v3_0.pdf">StickIt! board manual</a> will tell you how to set up your board, and it will provide some generic instructions for how to use PMOD and Wing modules. And each StickIt! module you buy from XESS will come with design examples that show you how to use that particular module. Those may also help if you're using a module from another manufacturer.</p>
<h2 class="toggle_title">How does the StickIt! board get power?</h2>
<p class="toggle_body">The StickIt! board can get power from a USB port via the attached XuLA board. Or you can supply power to both the StickIt! board and the XuLA boards with an external power adapter. Or you can use a combination of both methods.</p>
<h2 class="toggle_title">How much power does the StickIt! board need?</h2>
<p class="toggle_body">The StickIt! board itself doesn't really consume any power. (Well, the linear voltage regulators do take <em>some</em> power.) It serves mainly as a pass-through device between the XuLA board and whatever modules are plugged into the PMOD and Wing sockets.</p>
</div>



