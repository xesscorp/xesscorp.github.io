<div class="StdPageBody">
<p>The XSA-3S1000 Board keeps the same form-factor as our popular <a href="../../../prods/prod026.php">XSA-100 Board</a> while increasing the logic density to 1,000,000 gates with a Spartan-3 XC3S1000 FPGA.&nbsp; And it does it for only $199!</p>
<p>The FPGA is combined with a 32 MByte synchronous DRAM and 2 MByte Flash to give you the resources for building a complete, soft-core RISC microcontroller system!&nbsp; Or anything else you might think of...</p>
<p>Up to four unique bitstreams can be stored in the Flash and you can set the switches to select which bitstream configures the FPGA when power is applied.&nbsp; Or you can download directly to the FPGA through the parallel port with the XSTOOLs utilities we provide.&nbsp; The interface CPLD on the XSA-3S1000 also supports downloading with XILINX iMPACT and circuit test/debug with ChipScope using our simple downloading cable.&nbsp; No more expensive XILINX cables!</p>
<p>In addition to the larger FPGA, SDRAM and Flash chips, you also get a VGA port that produces vivid graphics in 512 colors. And the prototyping header gives you 65 general-purpose I/O pins that are completely free for building interfaces to external devices. And you can access a variety of interfaces by inserting the XSA-3S1000 into one of our <a href="../../../prods/prod042.php">XStend 4.0 Boards</a>.</p>
<p>Think it will be a big switch to move to the XSA-3S1000?&nbsp; Well, we provide all the software utilities for programming the FPGA, and downloading and uploading the RAM and Flash.&nbsp; And we make all the source code available for you to play with!&nbsp; How about design examples?&nbsp; We have parameterized modules for interfacing to the PS/2 keyboard port, displaying images through the VGA port, and reading/writing to the synchronous DRAM as if it were a simple static RAM.&nbsp; And we will be adapting all the chapters of our <a href="http://www.xess.com/prods/prod046.php">Pragmatic Logic Design</a> online text to support the XSA-3S1000.</p>
